    public void FiltrarDatos() {
        Conexion conexion = new Conexion();
        String sql;
        ResultSet rs;
        conexion.conectarBD();
        switch (cmbSelect.getSelectedItem().toString()) {
            case "Nombre":
                sql = "SELECT * FROM producto WHERE Nombre LIKE '%" + txtDatos.getText() + "%'";
                break;
            case "Categoria":
                sql = "SELECT * FROM producto WHERE Idcategoria LIKE '%" + txtDatos.getText() + "%'";
                break;
            case "Proveedor":
                sql = "SELECT * FROM producto WHERE IdProveedor LIKE '%" + txtDatos.getText() + "%'";
                break;
            case "Precio Unidad":
                sql = "SELECT * FROM producto WHERE Precio LIKE '%" + txtDatos.getText() + "%'";
                break;
            default:
                sql = " ";
                break;
        }
        if (sql.contains("SELECT")) {
            rs = conexion.seleccionar(sql);
            if (rs != null) {
                tblProducto.setModel(conexion.cargarEnTabla(rs));
                lblTotal.setText("TOTAL: " + tblProducto.getRowCount());
            }
        }
        conexion.desconectarBD();
    }
